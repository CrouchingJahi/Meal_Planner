// The size classes are not known at compile time and thus had to be added to ailwind config - if this size is changed, it will need to be added there too
function getIconClasses (className: string = '') {
  if (className) {
    if (className.includes('size-')) {
      return className
    } else {
      return `size-8 ${className}`
    }
  } else {
    return `size-8`
  }
}

